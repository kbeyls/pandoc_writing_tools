Writer.Inline.Link = function(link)
    local attr = pandoc.utils.stringify(link.attr.identifier) ~= "" and
        ' id="' .. escape_html(pandoc.utils.stringify(link.attr.identifier)) .. '"' or ""
    local classes = #link.attr.classes > 0 and
        ' class="' .. table.concat(link.attr.classes, " ") .. '"' or ""
    local content = Writer.Inlines(link.content)
    local url = escape_html(link.target)
    -- if the target is to an anchor in the same document, use a confluence anchor link
    if url:sub(1, 1) == "#" then
        return string.format('<ac:link ac:anchor="%s"><ac:link-body>%s</ac:link-body></ac:link>',
            escape_html(url:sub(2)), content)
    end
    return string.format('<a href="%s"%s%s>%s</a>', url, attr, classes, content)
end

Writer.Inline.Cite = function(cite)
    local content = Writer.Inlines(cite.content)
    return string.format("<cite>%s</cite>", content)
end

local note_counter = 0

Writer.Inline.Note = function(note)
    local content = Writer.Blocks(note.content)
    note_counter = note_counter + 1
    local note_indicator = string.format('<sup>[%d]</sup>', note_counter)
    return {
        note_indicator,
        '\n<ac:structured-macro ac:name="float" ac:schema-version="1">',
        '<ac:parameter ac:name="side">right</ac:parameter>',
        '<ac:parameter ac:name="width">150px</ac:parameter>',
        '<ac:rich-text-body>',
          '\n  <ac:structured-macro ac:name="bgcolor" ac:schema-version="1">',
          '<ac:parameter ac:name="color">#FFFCF2</ac:parameter>',
          '<ac:parameter ac:name="atlassian-macro-output-type">INLINE</ac:parameter>',
            '<ac:rich-text-body>\n',
            note_indicator,
            content,
            '\n  </ac:rich-text-body>',
          '</ac:structured-macro>',
        '\n</ac:rich-text-body>',
        '</ac:structured-macro>'
    }
end

local function get_anchor_if_id(el)
    if pandoc.utils.stringify(el.attr.identifier) ~= "" then
        local anchor = create_anchor(pandoc.utils.stringify(el.attr.identifier))
        return anchor
    else
        return ""
    end
end

local function process_specific_spans(span)
    local content = pandoc.utils.stringify(span.content)
    local anchor = get_anchor_if_id(span)
    if #span.attr.classes > 0 then
        for _, class in ipairs(span.attr.classes) do
            -- TODO: do I need to do special processing for index spans?
            --if class == "index" then
            --    -- Extract the entry from the content
            --    local entry = content:match('entry="(.-)"')
            --    if entry then
            --        return string.format(
            --            '<ac:structured-macro ac:name="index-term"><ac:parameter ac:name="term">%s</ac:parameter></ac:structured-macro>',
            --            escape_html(entry))
            --    end
            if class == "todo" then
                return anchor .. string.format(
                    '<span style="background-color: yellow; color: red; font-weight: bold;">TODO: %s</span>',
                    escape_html(content))
            elseif class == "index-entry" then
                return {anchor, Writer.Inlines(span.content), '<br/>'} -- let the content be processed normally
            elseif class == "index-entry-indentation" then
                return '&nbsp;&nbsp;&nbsp;&nbsp;' .. anchor .. Writer.Inlines(span.content) -- indent with non-breaking spaces
            end
        end
    end
    return nil
end

Writer.Inline.Span = function(span)
    local specific = process_specific_spans(span)
    if specific then
        return specific
    end
    -- Default span processing
    local attr = pandoc.utils.stringify(span.attr.identifier) ~= "" and
        ' id="' .. escape_html(pandoc.utils.stringify(span.attr.identifier)) .. '"' or ""
    local classes = #span.attr.classes > 0 and
        ' class="' .. table.concat(span.attr.classes, " ") .. '"' or ""
    local content = Writer.Inlines(span.content) --pandoc.utils.stringify(span.content)
    local result = get_anchor_if_id(span)
    --logging.error("Span content: " .. content)
    --logging.error("Span attr: " .. attr)
    --logging.error("Span classes: " .. classes)
    result = result .. string.format("<span%s%s>%s</span>", attr, classes, content)
    return result
end

local function split_literal(str, sep)
  local parts = {}
  local plain_sep = str:find(sep, 1, true) and sep or sep  -- true flag = plain find
  local last = 1
  local i, j = str:find(sep, last, true)
  while i do
    table.insert(parts, str:sub(last, i-1))
    last = j + 1
    i, j = str:find(sep, last, true)
  end
  table.insert(parts, str:sub(last))
  return parts
end

local function process_specific_divs(div)
    local anchor = get_anchor_if_id(div)
    if #div.attr.classes > 0 then
        for _, class in ipairs(div.attr.classes) do
            if class == "index-entry-list" then
                -- todo put half of entries in one cell and half in the other
                -- just split the list of elements in half for now and assumes that
                -- works well enough
                local content = pandoc.layout.render(Writer.Blocks(div.content))
                --logging.error("Index entry list content: ", content)
                -- content is a strings.
                -- First, in a hack, remove '<p></p>' from content, and replace it with <br/>
                content = content:gsub("<p></p>", "<br/>")
                -- now split content on <br/> tags
                local entries = split_literal(content, "<br/>")
                local half = math.ceil(#entries / 2)
                -- logging.error("Entries: ", table.concat(entries, "|"))
                -- logging.error("Half: ", half)
                -- logging.error("Left entries: ", table.concat(entries, 1, half), "|")
                -- logging.error("Right entries: ", table.concat(entries, half + 1), "|")
                -- now create left and right content
                local left_content = table.concat(entries, "<br/>", 1, half)
                local right_content = table.concat(entries, "<br/>", half + 1)
                return {
                    anchor,
                    '</ac:layout-cell>\n',
                    '</ac:layout-section>\n',
                    '<ac:layout-section ac:type="two_equal">\n',
                    '<ac:layout-cell>\n',
                    '<ac:structured-macro ac:name="panel">\n',
                    '<ac:parameter ac:name="bgColor">#EFEFEF</ac:parameter>\n',
                    '<ac:rich-text-body>\n',
                    left_content,
                    '</ac:rich-text-body>\n',
                    '</ac:structured-macro>\n',
                    '</ac:layout-cell>\n',
                    '<ac:layout-cell>\n',
                    '<ac:structured-macro ac:name="panel">\n',
                    '<ac:parameter ac:name="bgColor">#EFEFEF</ac:parameter>\n',
                    '<ac:rich-text-body>\n',
                    right_content,
                    '</ac:rich-text-body>\n',
                    '</ac:structured-macro>\n',
                    '</ac:layout-cell>\n',
                    '</ac:layout-section>\n',
                    '<ac:layout-section ac:type="single">\n<ac:layout-cell>\n',
                }
            elseif class == "TODO" then
                local content = pandoc.utils.stringify(div.content)
                return anchor .. string.format(
                    '<div style="background-color: yellow; color: red; font-weight: bold;">TODO: %s</div>',
                    escape_html(content))
            --elseif class == "csl-entry" then
            --    return { '\n<p>', Writer.Blocks(div.content), '</p>' }
            elseif class == "csl-entry" then
                return {
                    anchor,
                    '<ac:structured-macro ac:name="panel">\n',
                    '  <ac:parameter ac:name="bgColor">#EFEFEF</ac:parameter>\n',
                    '  <ac:rich-text-body>\n',
                    Writer.Blocks(div.content),
                    '  </ac:rich-text-body>\n',
                    '</ac:structured-macro>\n'}
                -- return { '\n<div><br/>', anchor, Writer.Blocks(div.content), '</div>\n' }
            elseif class == "smaller" then
                return {
                    anchor,
                    '<ac:structured-macro ac:name="expand" ac:schema-version="1"><ac:rich-text-body>',
                    Writer.Blocks(div.content),
                    '</ac:rich-text-body></ac:structured-macro>',
                }
            elseif class == "definition" or class == "example" then
                return {
                    anchor,
                    '<ac:structured-macro ac:name="panel" ac:schema-version="1">',
                    '<ac:parameter ac:name="title">',
                    div.attr.attributes["caption"],
                    '</ac:parameter>',
                    '<ac:rich-text-body>',
                    Writer.Blocks(div.content),
                    '</ac:rich-text-body>',
                    '</ac:structured-macro>',
                }
            elseif class == "toc-list" then
                -- handled in Div function below
                return {
                    anchor,
                    '<div style="margin-left: 40px;">',
                    Writer.Blocks(div.content),
                    '</div>\n'
                }
            end
        end
    end
    return nil
end

Writer.Block.Div = function(div)
    local specific = process_specific_divs(div)
    if specific then
        return specific
    end
    local attr = pandoc.utils.stringify(div.attr.identifier) ~= "" and
        ' id="' .. escape_html(pandoc.utils.stringify(div.attr.identifier)) .. '"' or ""
    local classes = #div.attr.classes > 0 and
        ' class="' .. table.concat(div.attr.classes, " ") .. '"' or ""
    local content = Writer.Blocks(div.content) --pandoc.utils.stringify(div.content)
    local result = get_anchor_if_id(div)
    result = result .. string.format("<div%s%s>\n%s\n</div>\n", attr, classes, content)
    return result
end

