Writer = pandoc.scaffolding.Writer

Writer.Inline.Str = function(str)
    return escape_html(str.text)
end

Writer.Inline.Space = function(space)
    return " "
end

local function create_anchor(id)
    return string.format(
        '<ac:structured-macro ac:name="anchor" ac:schema-version="1"><ac:parameter ac:name="">%s</ac:parameter></ac:structured-macro>',
        escape_html(id))
end

Writer.Block.Header = function(header)
    local tag = "h" .. header.level
    local attr = pandoc.utils.stringify(header.attr.identifier) ~= "" and
        ' id="' .. escape_html(pandoc.utils.stringify(header.attr.identifier)) .. '"' or ""
    local classes = #header.attr.classes > 0 and
        ' class="' .. table.concat(header.attr.classes, " ") .. '"' or ""
    local content = pandoc.utils.stringify(header.content)
    local anchor = create_anchor(pandoc.utils.stringify(header.attr.identifier))
    local section_number = header.attr.attributes["section-number"]
    if section_number then
        content = section_number .. " " .. content
    end
    return string.format("%s<%s%s%s>%s</%s>\n", anchor, tag, attr, classes, escape_html(content), tag)
end

Writer.Block.Para = function(para)
    -- iterate over para.content to handle inlines properly
    return { "<p>", Writer.Inlines(para.content), "</p>\n" }
end

Writer.Block.Plain = function(plain)
    return { "", Writer.Inlines(plain.content), "\n" }
end

Writer.Block.DefinitionList = function(deflist)
    local items = {}
    for _, item in ipairs(deflist.content) do
        local term = Writer.Inlines(item[1])
        local definitions = {}
        for _, def in ipairs(item[2]) do
            table.insert(definitions, Writer.Blocks(def))
        end
        table.insert(items, string.format("<dt>%s</dt>\n<dd>%s</dd>\n", term, table.concat(definitions, "\n")))
    end
    return string.format("<dl>\n%s\n</dl>\n", table.concat(items, "\n"))
end

Writer.Inline.Code = function(code)
    local attr = pandoc.utils.stringify(code.attr.identifier) ~= "" and
        ' id="' .. escape_html(pandoc.utils.stringify(code.attr.identifier)) .. '"' or ""
    local classes = #code.attr.classes > 0 and
        ' class="' .. table.concat(code.attr.classes, " ") .. '"' or ""
    local content = escape_html(code.text)
    return string.format("<code%s%s>%s</code>", attr, classes, content)
end

Writer.Inline.Quoted = function(quoted)
    local quote_type = quoted.quote_type == "Single" and "‘" or "“"
    local end_quote_type = quoted.quote_type == "Single" and "’" or "”"
    local content = pandoc.utils.stringify(quoted.content)
    return string.format("%s%s%s", quote_type, escape_html(content), end_quote_type)
end

Writer.Inline.SoftBreak = function()
    return " "
end

Writer.Inline.LineBreak = function()
    return "<br/>"
end

Writer.Inline.RawInline = function(raw)
    if raw.format:match 'html' then
        return raw.text
    end
    return ""
end

