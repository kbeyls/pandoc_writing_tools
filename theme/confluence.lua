-- This lua script is a pandoc writer that generates confluence XHTML storage format.
-- It is based on the default HTML writer, but with modifications to suit confluence.
-- It supports a subset of pandoc's markdown features, and is intended to be used
-- with pandoc's --to=confluence option.
-- It also supports some custom metadata fields for confluence pages.
-- See https://pandoc.org/MANUAL.html#extension-confluence for details.
-- See https://developer.atlassian.com/server/confluence/confluence-storage-format/ for details.
-- Note: This writer does not support all features of confluence, and may produce
-- invalid or incomplete output in some cases.
-- Use at your own risk.
local function add_tools_root_to_package_path()
  local script = PANDOC_SCRIPT_FILE
  if not script then
    return
  end
  local script_dir = script:match("(.*/)")
  if not script_dir then
    return
  end
  local tools_root = script_dir .. ".."
  package.path = table.concat({
    tools_root .. "/?.lua",
    tools_root .. "/?/init.lua",
    package.path,
  }, ";")
end

add_tools_root_to_package_path()

