process_table_cell = function(cell, cell_tag)
    local content = Writer.Blocks(cell.contents)
    return string.format("<%s>%s</%s>", cell_tag, content, cell_tag)
end

Writer.Block.Table = function(table_)
    -- process header
    local header_rows = {}
    for _, row in ipairs(table_.head.rows) do
        local row_cells = {}
        for _, cell in ipairs(row.cells) do
            local cell_content = process_table_cell(cell, "th")
            table.insert(row_cells, cell_content)
        end
        table.insert(header_rows, string.format("<tr>\n%s\n</tr>", table.concat(row_cells, "\n")))
    end
    -- process body
    local rows = {}
    for _, body in ipairs(table_.bodies) do
        for _, row in ipairs(body.body) do
            local row_cells = {}
            for _, cell in ipairs(row.cells) do
                local cell_content = process_table_cell(cell, "td")
                table.insert(row_cells, cell_content)
            end
            table.insert(rows, string.format("<tr>\n%s\n</tr>", table.concat(row_cells, "\n")))
        end
    end
    return string.format("<table><thead>\n%s\n</thead>\n<tbody>\n%s\n</tbody></table>\n",
        table.concat(header_rows, "\n"), table.concat(rows, "\n"))
end

Writer.Block.HorizontalRule = function()
